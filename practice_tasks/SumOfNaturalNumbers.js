//4️⃣ Print the sum of first 10 natural numbers
//for Loop
let sum = 0;

for (let i = 1; i <= 10; i++) {
    sum = sum + i;
    n = i;
}
console.log("sum of first" + " " + n + " " + "natural numbers is :" + sum);

//while loop
let j = 1;
let total = 0;
while (j <= 10) {
    total = total + j;
    m = j;
    j++;
}
console.log("total of first" + " " + m + " " + "natural numbers is :" + total);
